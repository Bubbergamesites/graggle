        async function openGame(file) {
            const modal = document.getElementById('gameModal');
            const frame = document.getElementById('gameFrame');
            const title = document.getElementById('modalTitle');
            
            title.textContent = `Loading: ${file}...`;
            modal.style.display = 'flex';
            activeGameName = file.includes('.') ? file : file + '.html';

            const url = `https://cdn.jsdelivr.net/gh/Bubbergamesites/ugs-singlefile@main/UGS-Files/cl${encodeURIComponent(activeGameName)}`;

            try {
                const response = await fetch(url);
                rawGameHtml = await response.text();
                
                if (currentBlobUrl) URL.revokeObjectURL(currentBlobUrl);
                const blob = new Blob([rawGameHtml], { type: 'text/html' });
                currentBlobUrl = URL.createObjectURL(blob);
                
                frame.src = currentBlobUrl;
                title.textContent = file;
            } catch (err) {
                title.textContent = "Error Loading Game";
                console.error(err);
            }
        }

